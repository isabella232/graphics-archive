// Global vars
var pymChild = null;
var isMobile = false;
var dataSeries = [];

/*
 * Initialize graphic
 */
var onWindowLoaded = function() {
    if (Modernizr.svg) {
        formatData();

        pymChild = new pym.Child({
            renderCallback: render
        });
    } else {
        pymChild = new pym.Child({});
    }

    pymChild.onMessage('on-screen', function(bucket) {
        ANALYTICS.trackEvent('on-screen', bucket);
    });
    pymChild.onMessage('scroll-depth', function(data) {
        data = JSON.parse(data);
        ANALYTICS.trackEvent('scroll-depth', data.percent, data.seconds);
    });
}

/*
 * Format graphic data for processing by D3.
 */
var formatData = function() {
    DATA.forEach(function(d) {
        d['date'] = d3.time.format('%Y').parse(d['date']);

        for (var key in d) {
            if (key != 'date' && d[key] != null && d[key].length > 0) {
                d[key] = +d[key];
            }
        }
    });

    /*
     * Restructure tabular data for easier charting.
     */
    for (var column in DATA[0]) {
        if (column == 'date') {
            continue;
        }

        dataSeries.push({
            'name': column,
            'values': DATA.map(function(d) {
                return {
                    'date': d['date'],
                    'amt': d[column]
                };
    // filter out empty data. uncomment this if you have inconsistent data.
    //        }).filter(function(d) {
    //            return d['amt'] != null;
            })
        });
    }
}

/*
 * Render the graphic(s). Called by pym with the container width.
 */
var render = function(containerWidth) {
    if (!containerWidth) {
        containerWidth = DEFAULT_WIDTH;
    }

    if (containerWidth <= MOBILE_THRESHOLD) {
        isMobile = true;
    } else {
        isMobile = false;
    }

    // Render the chart!
    renderAreaChart({
        container: '#line-chart',
        width: containerWidth,
        data: dataSeries
    });

    // Update iframe
    if (pymChild) {
        pymChild.sendHeight();
    }
}

/*
 * Render an area chart.
 */
var renderAreaChart = function(config) {
    /*
     * Setup
     */
    var dateColumn = 'date';
    var valueColumn = 'amt';

    var aspectWidth = isMobile ? 4 : 16;
    var aspectHeight = isMobile ? 3 : 9;

    var margins = {
        top: 5,
        right: 1,
        bottom: 20,
        left: 45
    };

    var ticksX = 10;
    var ticksY = 5;
    var roundTicksFactor = 10000;

    // Mobile
    if (isMobile) {
        ticksX = 5;
        ticksY = 5;
        margins['left'] = 30;
    }

    // Calculate actual chart dimensions
    var chartWidth = config['width'] - margins['left'] - margins['right'];
    var chartHeight = Math.ceil((config['width'] * aspectHeight) / aspectWidth) - margins['top'] - margins['bottom'];

    // Clear existing graphic (for redraw)
    var containerElement = d3.select(config['container']);
    containerElement.html('');

    /*
     * Create D3 scale objects.
     */
    var xScale = d3.time.scale()
        .domain(d3.extent(config['data'][0]['values'], function(d) {
            return d['date'];
        }))
        .range([ 0, chartWidth ])

    var min = d3.min(config['data'], function(d) {
        return d3.min(d['values'], function(v) {
            return Math.floor(v[valueColumn] / roundTicksFactor) * roundTicksFactor;
        })
    });

    if (min > 0) {
        min = 0;
    }

    var max = 25000;

    var yScale = d3.scale.linear()
        .domain([min, max])
        .range([chartHeight, 0]);

    var colorScale = d3.scale.ordinal()
        .domain(_.pluck(config['data'], 'name'))
        // .range([ COLORS['teal1'], COLORS['teal2'], COLORS['teal3'], COLORS['teal4'], COLORS['teal5'], '#ddd' ]);
        // .range([ '#17807e','#66957b','#99a976','#c6be71','#f3d469', '#ddd' ]);
        .range([ '#0b403f','#1b605e','#38807e','#5ea09e','#8bc0bf', '#ddd' ]);

    /*
     * Create the root SVG element.
     */
    var chartWrapper = containerElement.append('div')
        .attr('class', 'graphic-wrapper');

    var chartElement = chartWrapper.append('svg')
        .attr('width', chartWidth + margins['left'] + margins['right'])
        .attr('height', chartHeight + margins['top'] + margins['bottom'])
        .append('g')
        .attr('transform', 'translate(' + margins['left'] + ',' + margins['top'] + ')');

    /*
     * Create D3 axes.
     */
    var xAxis = d3.svg.axis()
        .scale(xScale)
        .orient('bottom')
        .ticks(ticksX)
        .tickFormat(function(d, i) {
            if (isMobile) {
                return '\u2019' + fmtYearAbbrev(d);
            } else {
                return fmtYearFull(d);
            }
        });

    var yAxis = d3.svg.axis()
        .scale(yScale)
        .orient('left')
        .ticks(ticksY)
        .tickFormat(function(d) {
            if (d == 0) {
                return 0;
            } else {
                if (isMobile) {
                    var val = d/1000;
                    return val.toFixed(0) + 'K';
                } else {
                    return fmtComma(d);
                }
            }
        });

    /*
     * Render axes to chart.
     */
    chartElement.append('g')
        .attr('class', 'x axis')
        .attr('transform', makeTranslate(0, chartHeight))
        .call(xAxis);

    chartElement.append('g')
        .attr('class', 'y axis')
        .call(yAxis);

    /*
     * Render grid to chart.
     */
    var xAxisGrid = function() {
        return xAxis;
    }

    var yAxisGrid = function() {
        return yAxis;
    }

    chartElement.append('g')
        .attr('class', 'x grid')
        .attr('transform', makeTranslate(0, chartHeight))
        .call(xAxisGrid()
            .tickSize(-chartHeight, 0, 0)
            .tickFormat('')
        );

    chartElement.append('g')
        .attr('class', 'y grid')
        .call(yAxisGrid()
            .tickSize(-chartWidth, 0, 0)
            .tickFormat('')
        );

    /*
     * Render 0 value line.
     */
    if (min < 0) {
        chartElement.append('line')
            .attr('class', 'zero-line')
            .attr('x1', 0)
            .attr('x2', chartWidth)
            .attr('y1', yScale(0))
            .attr('y2', yScale(0));
    }

    /*
     * Render lines to chart.
     */
    var area = d3.svg.area()
        .interpolate('monotone')
        .x(function(d) {
            return xScale(d[dateColumn]);
        })
        .y0(function(d) {
            return yScale(d['y0']);
        })
        .y1(function(d) {
            return yScale(d['y0'] + d[valueColumn]);
        });

    var stack = d3.layout.stack()
        .values(function(d) { return d['values']; })
        .x(function(d) {
            return d[dateColumn];
        })
        .y(function(d) {
            return d[valueColumn];
        });

    var areas = stack(config['data']);

    var area_g = chartElement.append('g')
        .attr('class', 'areas')
        .selectAll('.area-g')
            .data(areas)
                .enter()
                .append('g')
                    .attr('class', 'area-g');

    area_g
        .append('path')
            .attr('class', function(d, i) {
                return 'area ' + classify(d['name']);
            })
            .attr('fill', function(d,i) {
                return colorScale(d['name']);
            })
            .attr('d', function(d) {
                return area(d['values']);
            });

    var value_g = chartElement.append('g')
        .attr('class', 'area-values value')
        .selectAll('text')
        .data(areas)
        .enter()
            .append('text')
                .text(function(d, i) {
                    return d['name'];
                })
                .attr('class', function(d,i) {
                    return classify(d['name']);
                })
                .attr('dx', function(d) {
                    var dx = 0;
                    switch(d['name']) {
                        case 'South Korea':
                            dx = isMobile ? 5 : -10;
                            break;
                        case 'Guatemala':
                            dx = isMobile ? 0 : 15;
                            break;
                    }
                    return dx;
                })
                .attr('x', function(d,i) {
                    var maxVal = getMaxValue(d, valueColumn);
                    return xScale(maxVal[dateColumn]);
                })
                .attr('y', function(d,i) {
                    var maxVal = getMaxValue(d, valueColumn);
                    var yPos =  yScale(maxVal['y0'] + (maxVal['y'] / 2)) + 6;
                    return yPos;
                });
}

var getMaxValue = function(s, valueColumn) {
    var series = s['name'];
    var values = s['values'];

    var maxValue = d3.max(values, function(v) {
        return v[valueColumn];
    });

    var maxValData = values.filter(function(v) {
        return v[valueColumn] == maxValue;
    })[0];

    switch(s['name']) {
        case 'All other countries':
            maxValData = values[5];
            break;
        case 'Guatemala':
            maxValData = values[6];
            break;
        case 'South Korea':
            maxValData = values[5];
            break;
    }

    return maxValData;
}


